package ethernet_802_3

import (
	"bytes"
	"testing"
)

func TestFrame(t *testing.T) {
	stpFrame := []byte{
		0x01, 0x80, 0xc2, 0x00, 0x00, 0x00, 0xee, 0x82, 0xa7, 0x6f, 0x0d, 0x66, 0x00, 0x26, 0x42, 0x42,
		0x03, 0x00, 0x00, 0x00, 0x00, 0x00, 0x80, 0x00, 0x8a, 0xf3, 0x9c, 0x27, 0x13, 0x4d, 0x00, 0x00,
		0x00, 0x20, 0x00, 0x00, 0x80, 0x00, 0x8a, 0xf3, 0x9c, 0x27, 0x13, 0x4d, 0x80, 0x03, 0x00, 0x00, 0x14, 0x00,
		0x00, 0x30, 0x02, 0x00, 0x0f,
	}
	f := Frame{}
	err := f.UnmarshalBinary(stpFrame)
	if err != nil {
		t.Errorf("failed to unmarshal binary due to error %v", err)
	}
	b, err := f.MarshalBinary()
	if err != nil {
		t.Errorf("failed to marshal binary due to error %v", err)
	}
	if !bytes.Equal(b, stpFrame) {
		t.Errorf("two byte frames are not equal.\nb1: %v\nb2: %v", stpFrame, b)
	}
}
